package poo23;

public class Tempo {
    private String oraArrivo; 
    private String oraUscita;
    
    public Tempo(String oraArrivo, String oraUscita) {
        this.oraArrivo = oraArrivo;
        this.oraUscita = oraUscita;
    }

    public String getOraArrivo() {
        return oraArrivo;
    }

    public void setOraArrivo(String oraArrivo) {
        this.oraArrivo = oraArrivo;
    }

    public String getOraUscita() {
        return oraUscita;
    }

    public void setOraUscita(String oraUscita) {
        this.oraUscita = oraUscita;
    } 

    
}
