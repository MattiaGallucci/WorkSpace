package poo23;

public class Categoria {
	private String nome;
	private int coefficiente;
	
	public Categoria(String nome, int coefficiente) {
		this.nome = nome;
		this.coefficiente = coefficiente;
	}

	public String getNome() {
		return nome;
	}

	public int getCoefficiente() {
		return coefficiente;
	}
	
	
}
